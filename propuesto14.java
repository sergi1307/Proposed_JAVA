public class propuesto14 {
    public static void main(String[] args) {

        int suma = 0;
        int producto = 1;

        for (int i = 1; i <= 10; i++) {
            suma = suma + i;
            producto = producto * i;
        }

        System.out.println("El resultado de la suma es: " + suma);
        System.out.println("El resultado del producto es: " + producto);

    }
}
